#include <iostream>
using namespace std;

int main ()
{
    double a;
    double b;
    
    cout << "Sum, difference, and product of 2 numbers..." << endl;
    
    cout << "Enter the first number: ";
    cin >> a;
    
    cout << "Enter the second number: ";
    cin >> b;
    
    cout << "Sum: " << a + b << endl;
    cout << "Difference: " << a - b << endl;
    cout << "Product: " << a * b << endl;
    
    return 0;
}
